# Checking the range operator in python
for num in range(0,11,2):
    print(num)

''' Here we are printing all the number from 0 - 9 with a step of 2.
    In the range function all the numbers would be printed except the last number. If we want to include the 10 in the above range we should include the number 11 in the range'''
list(range(0,15,2))
